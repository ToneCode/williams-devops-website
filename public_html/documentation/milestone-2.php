		<p>Matthew is viewing the site on his desktop computer at the office, with a 27" screen, using Safari. He has 29
			other tabs open. From LinkedIn, he links to my personal website which opens in a new window. In the next 3
			minutes, here is an outline of Matthew's interactions with my site: </p>
		<ol>
			<li>Lands on Home page. Views a "quote where I describe my work flow" Clicks call to action that navigates to
				the About Me page.
			</li>
			<li>Views my core skills featured in a contrasting side panel. Finds my professional history to be outstanding.
